class Pet {
  final String id;
  final String name;
  final String category;
  final String image;
  final double price;
  final String breed;
  final int age;
  final String description;

  Pet({
    required this.id,
    required this.name,
    required this.category,
    required this.image,
    required this.price,
    required this.breed,
    required this.age,
    required this.description,
  });
}
